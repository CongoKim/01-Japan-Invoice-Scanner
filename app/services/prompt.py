def build_extraction_prompt(receipt_like: bool) -> str:
    """Build an extraction prompt tailored to invoice-vs-receipt document style."""
    appendix = RECEIPT_EXTRACTION_APPENDIX if receipt_like else INVOICE_EXTRACTION_APPENDIX
    return f"{EXTRACTION_PROMPT}{appendix}"


def build_arbitration_prompt(
    gemini_result: dict,
    openai_result: dict,
    diff_fields: list[str],
    *,
    receipt_like: bool = False,
) -> str:
    """Build prompt for Claude arbitration when Gemini and OpenAI disagree."""
    import json

    document_rule = (
        "- この書類はレシート・領収書・乗車券類として扱ってください。tax_verification は税基ではなく、票面に表示された税率別税額を出力してください。"
        if receipt_like
        else "- この書類は正式な請求書・インボイスとして扱ってください。消費税核定は税額ではなく、各税率に対応する金額基数（課税対象額）を出力してください。"
    )
